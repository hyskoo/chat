package kr.ac.chat.model;

public class Member {
	String mId;
	String mPw;
	String mAuth;
	public String getmId() {
		return mId;
	}
	public void setmId(String mId) {
		this.mId = mId;
	}
	public String getmPw() {
		return mPw;
	}
	public void setmPw(String mPw) {
		this.mPw = mPw;
	}
	public String getmAuth() {
		return mAuth;
	}
	public void setmAuth(String mAuth) {
		this.mAuth = mAuth;
	}
	
	
	
}
